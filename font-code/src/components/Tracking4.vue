<script setup>
import { ref, computed, onMounted } from 'vue';
import { ArrowDown } from '@element-plus/icons-vue';

const tableKey = ref(Date.now());

function refreshTable() {
  tableKey.value = Date.now(); // 改 key 强制重新渲染
}

const tabs = ['Transactions', 'Top Traders', 'Holders', 'My Positions', 'My Orders', 'DEV Insight'];
const activeTab = ref(tabs[0]);

const typeOptions = ['全部', '买入', '卖出', '加池子', '减池子', '烧币'];
const selectedType = ref('全部');

function changeTypeFilter(type) {
  selectedType.value = type;
}

const allData = ref([]);
const visibleCount = ref(10);
const pause = ref(false);
const shouldPause = computed(() => pause.value);
const tableRef = ref(null);

const visibleData = computed(() => {
  let result = allData.value;
  if (selectedType.value !== '全部') {
    result = result.filter((row) => row.type === selectedType.value);
  }
  return result.slice(0, visibleCount.value);
});

function formatAge(seconds) {
  if (seconds < 60) return `${seconds}s`;
  else if (seconds < 3600) return `${Math.floor(seconds / 60)}m`;
  else return `${Math.floor(seconds / 3600)}h`;
}

function updateAge() {
  const now = Date.now();
  allData.value.forEach((row) => {
    const sec = Math.floor((now - row.timestamp) / 1000);
    row.ageText = formatAge(sec);
  });
}

function createFakeData(offsetIndex = 0) {
  const now = Date.now();
  return Array.from({ length: 10 }).map((_, i) => {
    const totalOffset = (offsetIndex + i) * 5000;
    return {
      timestamp: now - totalOffset,
      ageText: '',
      type: typeOptions[(i % (typeOptions.length - 1)) + 1],
      amount: `${(0.3 + i * 0.1).toFixed(3)}K`,
      price: `$0.${(4500 + i).toFixed(4)}`,
      sol: (0.1 + i * 0.01).toFixed(5),
      usd: `$${(10 + i * 2.5).toFixed(2)}`,
      maker: `SoS${i.toString(36)}...vWv`,
    };
  });
}

function onScroll() {
  const el = tableRef.value?.$el?.querySelector('.el-scrollbar__wrap');
  if (!el) return;
  const { scrollTop, scrollHeight, clientHeight } = el;
  if (scrollTop + clientHeight >= scrollHeight - 10) {
    const more = createFakeData(allData.value.length);
    allData.value.push(...more);
    updateAge();
    visibleCount.value += 10;
  }
}

function switchTab(tab) {
  activeTab.value = tab;
  allData.value = [];
  visibleCount.value = 10;
  loadInitial();
  refreshTable();
}

function loadInitial() {
  allData.value = createFakeData();
  updateAge();
}

function startAutoUpdate() {
  setInterval(() => {
    if (!shouldPause.value) {
      const newRow = {
        timestamp: Date.now(),
        ageText: '',
        type: typeOptions[Math.floor(Math.random() * (typeOptions.length - 1)) + 1],
        amount: `${(Math.random() * 10).toFixed(2)}K`,
        price: `$0.${Math.floor(4500 + Math.random() * 100).toFixed(4)}`,
        sol: (0.1 + Math.random()).toFixed(5),
        usd: `$${(10 + Math.random() * 200).toFixed(2)}`,
        maker: `SoS${Math.random().toString(36).slice(2, 6)}...vWv`,
      };
      if (selectedType.value === '全部' || newRow.type === selectedType.value) {
        allData.value.unshift(newRow);
        updateAge();
      }
    }
  }, 5000);

  setInterval(updateAge, 1000);
}

onMounted(() => {
  loadInitial();
  startAutoUpdate();
});
</script>

